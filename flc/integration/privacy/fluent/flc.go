package main

import (
	"log"
	"net/http"

	"github.com/usalko/fluent/flc"
	"github.com/usalko/fluent/flc/gen"
)

func main() {
	// A usage for custom options to configure the code generator to use
	// an extension and inject external dependencies in the generated API.
	opts := []flc.Option{
		flc.Dependency(
			flc.DependencyType(&http.Client{}),
		),
		flc.FeatureNames(
			"privacy",
			"fluent_ql",
			"schema/snapshot",
		),
	}
	err := flc.Generate("./schema", &gen.Config{
		Header: `
			// This source code is licensed under the Apache 2.0 license found
			// in the LICENSE file in the root directory of this source tree.

			// Code generated by ent, DO NOT EDIT.
		`,
	}, opts...)
	if err != nil {
		log.Fatalf("running fluent codegen: %v", err)
	}
}
